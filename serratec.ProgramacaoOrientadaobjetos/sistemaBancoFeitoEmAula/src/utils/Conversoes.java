package utils;

public class Conversoes {
	
	public static double converteMilimetroParaCentimetro(double milimetro) {
		return milimetro / 10;
	}
	
	public static double converteMilimetroParaDecimetro(double milimetro) {
		return milimetro / 100;
	}
	
	public static double converteMilimetroParaMetro(double milimetro) {
		return milimetro / 1000;
	}
	
	public static double converteMilimetroParaKilometro(double milimetro) {
		return milimetro / 1000000;
	}
}
